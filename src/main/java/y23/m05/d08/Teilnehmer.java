package y23.m05.d08;

public class Teilnehmer {
    private String name;
    private String vorname;

    public Teilnehmer(String name, String vorname) {
        this.name = name;
        this.vorname = vorname;
    }

    public String getName() {
        return name;
    }

    public String getVorname() {
        return vorname;
    }
}
